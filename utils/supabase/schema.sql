-- 10. Create unified fleet requests table for approval workflow
CREATE TABLE IF NOT EXISTS fleet_requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  request_type VARCHAR NOT NULL, -- 'add_car', 'edit_car', 'add_driver', 'edit_driver', 'log_km', 'log_service', 'log_fuel', 'log_insurance', 'log_misc', 'assign_driver'
  payload JSONB NOT NULL,
  status VARCHAR DEFAULT 'pending', -- 'pending', 'approved', 'rejected'
  requested_by VARCHAR DEFAULT 'staff',
  created_at TIMESTAMPTZ DEFAULT now(),
  resolved_by VARCHAR DEFAULT NULL,
  resolved_at TIMESTAMPTZ DEFAULT NULL
);

ALTER TABLE fleet_requests DISABLE ROW LEVEL SECURITY;

