-- 12. Create driver payments tracking table
CREATE TABLE IF NOT EXISTS driver_payments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  driver_id BIGINT REFERENCES drivers(id) ON DELETE CASCADE,
  payment_date DATE NOT NULL,
  amount NUMERIC NOT NULL,
  type VARCHAR NOT NULL, -- 'salary_paid', 'deduction', 'advance'
  comment TEXT,
  created_at TIMESTAMPTZ DEFAULT now()
);

ALTER TABLE driver_payments DISABLE ROW LEVEL SECURITY;
